        GL11.glPushMatrix();
        GL11.glBlendFunc(770, 771);
        GL11.glEnable(3042);
        GL11.glLineWidth(1.2f);
        GL11.glDisable(3553);
        GL11.glDisable(2929);
        GL11.glDepthMask(false);
        s.setDrawStyle(100013);
        GL11.glTranslated(x - TuxHackTessellator.mc.getRenderManager().renderPosX, y - TuxHackTessellator.mc.getRenderManager().renderPosY, z - TuxHackTessellator.mc.getRenderManager().renderPosZ);
